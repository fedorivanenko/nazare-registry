import fs from "node:fs";
import path from "node:path";

const GENERATED_MARKER = "Generated by Nazare. Do not edit directly.";
const CSS_MODES = new Set(["normal", "preload"]);
const GENERATED_BRIDGE_SNIPPETS = new Set([
	"section-css",
	"section-css-preloads",
]);
const MODULE_KEY_PATTERN =
	/^(sections|snippets|behaviors)\/[a-z0-9]+(?:-[a-z0-9]+)*$/;
const STATIC_RENDER_PATTERN = /{%\s*render\s+(['"])([^'"]+)\1[^%]*%}/g;
const ANY_RENDER_PATTERN = /{%\s*render\s+([^%]+)%}/g;
const DATA_NAZARE_USE_PATTERN = /\bdata-nazare-use\s*=\s*(['"])(.*?)\1/g;
const CSS_DIRECTIVE_PATTERN =
	/{%\s*comment\s*%}\s*nazare:css\s+([^\s]+)\s*{%\s*endcomment\s*%}/g;

function slash(value) {
	return value.split(path.sep).join("/");
}

function pathExists(filePath) {
	return fs.existsSync(filePath);
}

function readText(filePath) {
	return fs.readFileSync(filePath, "utf8");
}

function writeText(filePath, content) {
	fs.mkdirSync(path.dirname(filePath), { recursive: true });
	fs.writeFileSync(filePath, content);
}

function listLiquidFiles(dirPath) {
	if (!pathExists(dirPath)) return [];

	return fs
		.readdirSync(dirPath, { withFileTypes: true })
		.filter((entry) => entry.isFile() && entry.name.endsWith(".liquid"))
		.map((entry) => path.join(dirPath, entry.name))
		.sort();
}

function renderedOutputIndex(source) {
	const candidates = [];
	const htmlIndex = source.search(/<[A-Za-z][^>]*>/);
	if (htmlIndex >= 0) candidates.push(htmlIndex);
	const outputIndex = source.search(/{{[\s\S]*?}}/);
	if (outputIndex >= 0) candidates.push(outputIndex);
	const renderIndex = source.search(/{%\s*render\b/);
	if (renderIndex >= 0) candidates.push(renderIndex);
	const rawTextIndex = source.search(/(^|\n)\s*[^\s<{][^\n]*/);
	if (rawTextIndex >= 0) candidates.push(rawTextIndex);

	return candidates.length === 0
		? Number.POSITIVE_INFINITY
		: Math.min(...candidates);
}

function parseCssDirective(source, relativePath, kind) {
	const matches = [...source.matchAll(CSS_DIRECTIVE_PATTERN)];

	if (matches.length === 0) return "normal";
	if (kind !== "section") {
		throw new Error(`CSS directive is only valid in sections: ${relativePath}`);
	}
	if (matches.length > 1) {
		throw new Error(`Duplicate CSS directive in ${relativePath}`);
	}

	const match = matches[0];
	const mode = match[1];
	if (!CSS_MODES.has(mode)) {
		throw new Error(`Invalid CSS directive value in ${relativePath}: ${mode}`);
	}
	if (match.index > renderedOutputIndex(source)) {
		throw new Error(
			`CSS directive must appear before rendered output in ${relativePath}`,
		);
	}

	return mode;
}

function extractStaticRenders(source) {
	return [...source.matchAll(STATIC_RENDER_PATTERN)]
		.map((match) => match[2])
		.filter((snippetName) => !GENERATED_BRIDGE_SNIPPETS.has(snippetName));
}

function hasDynamicRender(source) {
	for (const match of source.matchAll(ANY_RENDER_PATTERN)) {
		const expression = match[1].trim();
		if (!expression.startsWith("'") && !expression.startsWith('"')) {
			return true;
		}
	}

	return false;
}

function extractModuleKeys(source, relativePath) {
	const keys = [];

	for (const match of source.matchAll(DATA_NAZARE_USE_PATTERN)) {
		const value = match[2].trim();
		if (!value) {
			throw new Error(`Empty data-nazare-use in ${relativePath}`);
		}
		if (/[,\s]/.test(value)) {
			throw new Error(
				`data-nazare-use must contain exactly one module key in ${relativePath}`,
			);
		}
		if (!MODULE_KEY_PATTERN.test(value)) {
			throw new Error(
				`Invalid data-nazare-use module key in ${relativePath}: ${value}`,
			);
		}
		keys.push(value);
	}

	return keys;
}

function scanSnippet(root, snippetName, stack, warnings) {
	const relativePath = `snippets/${snippetName}.liquid`;
	const filePath = path.join(root, relativePath);
	if (!pathExists(filePath)) {
		throw new Error(`Missing snippet: ${relativePath}`);
	}
	if (stack.includes(snippetName)) {
		throw new Error(
			`Snippet render cycle: ${[...stack, snippetName].join(" -> ")}`,
		);
	}

	const source = readText(filePath);
	parseCssDirective(source, relativePath, "snippet");
	if (hasDynamicRender(source)) {
		warnings.push(`Dynamic render not followed in ${relativePath}`);
	}

	const dependencies = new Set([relativePath]);
	const moduleKeys = new Set(extractModuleKeys(source, relativePath));

	for (const child of extractStaticRenders(source)) {
		const scan = scanSnippet(root, child, [...stack, snippetName], warnings);
		for (const dependency of scan.dependencies) dependencies.add(dependency);
		for (const key of scan.moduleKeys) moduleKeys.add(key);
	}

	return { dependencies, moduleKeys };
}

export function scanTheme(root = process.cwd()) {
	const warnings = [];
	const sections = [];
	const moduleKeys = new Set();
	const sectionFiles = listLiquidFiles(path.join(root, "sections"));

	for (const filePath of sectionFiles) {
		const sectionName = path.basename(filePath, ".liquid");
		const relativePath = slash(path.relative(root, filePath));
		const source = readText(filePath);
		const cssMode = parseCssDirective(source, relativePath, "section");
		if (hasDynamicRender(source)) {
			warnings.push(`Dynamic render not followed in ${relativePath}`);
		}

		const sources = new Set([relativePath]);
		for (const key of extractModuleKeys(source, relativePath))
			moduleKeys.add(key);

		for (const snippetName of extractStaticRenders(source)) {
			const scan = scanSnippet(root, snippetName, [], warnings);
			for (const dependency of scan.dependencies) sources.add(dependency);
			for (const key of scan.moduleKeys) moduleKeys.add(key);
		}

		sections.push({ name: sectionName, cssMode, sources: [...sources].sort() });
	}

	for (const key of moduleKeys) {
		const modulePath = path.join(root, "scripts", `${key}.js`);
		if (!pathExists(modulePath)) {
			throw new Error(
				`Missing JS module for data-nazare-use: scripts/${key}.js`,
			);
		}
	}

	return {
		sections: sections.sort((a, b) => a.name.localeCompare(b.name)),
		moduleKeys: [...moduleKeys].sort(),
		warnings,
	};
}

function sectionCssContent(section) {
	const sources = section.sources
		.map((source) => `@source "../${source}";`)
		.join("\n");

	return `/* ${GENERATED_MARKER} */\n@import "tailwindcss/theme" source(none);\n@import "tailwindcss/utilities" source(none);\n\n${sources}\n`;
}

function sectionCssSnippet(sections) {
	const normalSections = sections.filter(
		(section) => section.cssMode === "normal",
	);
	const cases = normalSections
		.map(
			(section) =>
				`  {% when '${section.name}' %}\n    {{ '${section.name}.css' | asset_url | stylesheet_tag }}`,
		)
		.join("\n");

	return `{% comment %}\n  ${GENERATED_MARKER}\n{% endcomment %}\n\n{% case section_name %}\n${cases}\n{% endcase %}\n`;
}

function sectionCssPreloadsSnippet(sections) {
	const lines = sections
		.filter((section) => section.cssMode === "preload")
		.map(
			(section) =>
				`{{ '${section.name}.css' | asset_url | stylesheet_tag: preload: true }}`,
		)
		.join("\n");

	return `{% comment %}\n  ${GENERATED_MARKER}\n{% endcomment %}\n\n${lines}${lines ? "\n" : ""}`;
}

function runtimeContent() {
	return `// ${GENERATED_MARKER}\n\nconst modules = {\n  ...import.meta.glob("./sections/*.js"),\n  ...import.meta.glob("./snippets/*.js"),\n  ...import.meta.glob("./behaviors/*.js"),\n};\n\nconst modulePromises = new Map();\nconst mounted = new WeakMap();\n\nasync function importModule(key) {\n  const load = modules[\`./\${key}.js\`];\n\n  if (!load) {\n    console.warn(\`[nazare] Missing JS module for \${key}\`);\n    return null;\n  }\n\n  try {\n    return await load();\n  } catch (error) {\n    console.warn(\`[nazare] Failed to import JS module for \${key}\`, error);\n    return null;\n  }\n}\n\nfunction nazareNodes(root) {\n  const selector = "[data-nazare-use]";\n  const nodes = [...root.querySelectorAll(selector)];\n\n  if (root.matches?.(selector)) {\n    nodes.unshift(root);\n  }\n\n  return nodes;\n}\n\nexport async function initNazare(root = document) {\n  for (const node of nazareNodes(root)) {\n    const key = node.dataset.nazareUse;\n\n    if (mounted.get(node)?.key === key) continue;\n\n    if (!modulePromises.has(key)) {\n      modulePromises.set(key, importModule(key));\n    }\n\n    const module = await modulePromises.get(key);\n    if (!module?.init) continue;\n\n    try {\n      module.init(node);\n      mounted.set(node, { key, module });\n    } catch (error) {\n      console.warn(\`[nazare] Failed to initialize JS module for \${key}\`, error);\n    }\n  }\n}\n\nexport function destroyNazare(root = document) {\n  for (const node of nazareNodes(root)) {\n    const mount = mounted.get(node);\n    if (!mount) continue;\n\n    try {\n      mount.module.destroy?.(node);\n    } catch (error) {\n      console.warn(\`[nazare] Failed to destroy JS module for \${mount.key}\`, error);\n    } finally {\n      mounted.delete(node);\n    }\n  }\n}\n\ndocument.addEventListener("shopify:section:load", (event) => {\n  initNazare(event.target);\n});\n\ndocument.addEventListener("shopify:section:unload", (event) => {\n  destroyNazare(event.target);\n});\n\ninitNazare();\n`;
}

function removeStaleGeneratedSectionCss(root, sectionNames) {
	const stylesDir = path.join(root, "styles");
	if (!pathExists(stylesDir)) return;

	for (const filePath of fs.readdirSync(stylesDir)) {
		if (!filePath.endsWith(".css")) continue;
		const absolutePath = path.join(stylesDir, filePath);
		const source = readText(absolutePath);
		const sectionName = path.basename(filePath, ".css");
		if (source.includes(GENERATED_MARKER) && !sectionNames.has(sectionName)) {
			fs.rmSync(absolutePath);
		}
	}
}

export function generateThemeBuildFiles(root = process.cwd()) {
	const graph = scanTheme(root);
	const sectionNames = new Set(graph.sections.map((section) => section.name));

	removeStaleGeneratedSectionCss(root, sectionNames);

	for (const section of graph.sections) {
		writeText(
			path.join(root, "styles", `${section.name}.css`),
			sectionCssContent(section),
		);
	}

	writeText(path.join(root, "scripts", "theme.js"), runtimeContent());
	writeText(
		path.join(root, "snippets", "section-css.liquid"),
		sectionCssSnippet(graph.sections),
	);
	writeText(
		path.join(root, "snippets", "section-css-preloads.liquid"),
		sectionCssPreloadsSnippet(graph.sections),
	);

	return graph;
}

function rollupInput(graph) {
	const input = {
		theme: "scripts/theme.js",
	};

	for (const section of graph.sections) {
		input[section.name] = `styles/${section.name}.css`;
	}

	return input;
}

export function nazareThemePlugin() {
	let root = process.cwd();

	return {
		name: "nazare-theme-plugin",
		config(userConfig = {}) {
			root = path.resolve(userConfig.root ?? process.cwd());
			const graph = generateThemeBuildFiles(root);

			return {
				build: {
					rollupOptions: {
						input: rollupInput(graph),
					},
				},
			};
		},
		buildStart() {
			const graph = generateThemeBuildFiles(root);
			for (const warning of graph.warnings) {
				this.warn(warning);
			}
		},
	};
}
